"""Evaluation harness — runs ZugaWatch over the labeled corpus and reports
precision / recall / F1 / false-positive-rate + latency, overall and per
category. Writes benchmark/RESULTS.md.

    python benchmark/run.py            # print + write RESULTS.md
    python benchmark/run.py --quiet    # just write the file

Scenario-level detection: a scenario is "flagged" if ZugaWatch emits any finding
(chain scenarios) or any tool-definition drift (pin scenarios).
"""

from __future__ import annotations

import sys
import time
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(ROOT / "src"))

from zugawatch.anomaly import AnomalyEngine, CallChain  # noqa: E402
from zugawatch.pinning import PinStore, ToolDef, diff_pins  # noqa: E402

sys.path.insert(0, str(ROOT / "benchmark"))
from dataset import build_dataset  # noqa: E402


def _flag_chain(events) -> tuple[bool, list[str], float]:
    chain = CallChain()
    for c in events:
        chain.record(c["server"], c["tool"], c.get("args", {}), c.get("result", ""), c.get("description", ""), c.get("annotations"))
    t0 = time.perf_counter()
    findings = AnomalyEngine().analyze(chain)
    dt = (time.perf_counter() - t0) * 1000.0
    return bool(findings), sorted({f.rule_id for f in findings}), dt


def _flag_pins(baseline, live) -> tuple[bool, list[str], float]:
    store = PinStore.pin("svc", [ToolDef(t["name"], t.get("description", ""), t.get("input_schema", {})) for t in baseline])
    live_defs = [ToolDef(t["name"], t.get("description", ""), t.get("input_schema", {})) for t in live]
    t0 = time.perf_counter()
    drifts = diff_pins(store, live_defs)
    dt = (time.perf_counter() - t0) * 1000.0
    return bool(drifts), (["DRIFT"] if drifts else []), dt


def evaluate():
    ds = build_dataset()
    tp = fp = tn = fn = 0
    cats: dict[str, list[int]] = {}   # category -> [detected, total]
    latencies: list[float] = []
    misses: list[str] = []
    false_pos: list[str] = []

    for s in ds:
        if s["kind"] == "chain":
            flagged, rules, dt = _flag_chain(s["chain"])
        else:
            flagged, rules, dt = _flag_pins(s["baseline_tools"], s["live_tools"])
        latencies.append(dt)

        is_attack = s["label"] == "attack"
        if is_attack and flagged:
            tp += 1
        elif is_attack and not flagged:
            fn += 1
            misses.append(s["id"])
        elif not is_attack and flagged:
            fp += 1
            false_pos.append(f'{s["id"]} ({s["category"]})')
        else:
            tn += 1

        c = cats.setdefault(s["category"], [0, 0])
        c[1] += 1
        # for attacks "detected"=flagged; for benign "correct"=not flagged
        c[0] += int(flagged) if is_attack else int(not flagged)

    precision = tp / (tp + fp) if (tp + fp) else 0.0
    recall = tp / (tp + fn) if (tp + fn) else 0.0
    f1 = 2 * precision * recall / (precision + recall) if (precision + recall) else 0.0
    fpr = fp / (fp + tn) if (fp + tn) else 0.0
    acc = (tp + tn) / len(ds)
    latencies.sort()
    p95 = latencies[int(0.95 * (len(latencies) - 1))]
    mean = sum(latencies) / len(latencies)

    return {
        "n": len(ds), "tp": tp, "fp": fp, "tn": tn, "fn": fn,
        "precision": precision, "recall": recall, "f1": f1, "fpr": fpr, "accuracy": acc,
        "latency_mean_ms": mean, "latency_p95_ms": p95,
        "cats": cats, "misses": misses, "false_pos": false_pos,
    }


def render(r) -> str:
    lines = []
    lines.append("# MCP ZugaWatch — Benchmark Results\n")
    lines.append("Generated by `benchmark/run.py` over `benchmark/dataset.py` "
                 "(a labeled corpus of attack, benign, and evasion scenarios).\n")
    lines.append(f"**{r['n']} scenarios** · TP={r['tp']} FP={r['fp']} TN={r['tn']} FN={r['fn']}\n")
    lines.append("## Detection metrics\n")
    lines.append("| Metric | Value |")
    lines.append("|---|---|")
    lines.append(f"| Precision | {r['precision']:.3f} |")
    lines.append(f"| Recall | {r['recall']:.3f} |")
    lines.append(f"| F1 | {r['f1']:.3f} |")
    lines.append(f"| False-positive rate | {r['fpr']:.3f} |")
    lines.append(f"| Accuracy | {r['accuracy']:.3f} |")
    lines.append(f"| Latency mean | {r['latency_mean_ms']:.3f} ms |")
    lines.append(f"| Latency p95 | {r['latency_p95_ms']:.3f} ms |\n")
    lines.append("## Per-category detection (attacks: caught; benign: correctly cleared)\n")
    lines.append("| Category | Correct / Total | Rate |")
    lines.append("|---|---|---|")
    for cat in sorted(r["cats"]):
        ok, tot = r["cats"][cat]
        lines.append(f"| {cat} | {ok}/{tot} | {ok / tot:.2f} |")
    lines.append("")
    if r["misses"]:
        lines.append("## Missed attacks (false negatives) — reported honestly\n")
        lines.append("These are attacks ZugaWatch did NOT flag. They define the roadmap:\n")
        lines.append("```\n" + "\n".join(r["misses"]) + "\n```\n")
    if r["false_pos"]:
        lines.append("## False positives\n")
        lines.append("```\n" + "\n".join(r["false_pos"]) + "\n```\n")
    return "\n".join(lines)


def main(argv=None) -> int:
    argv = argv if argv is not None else sys.argv[1:]
    r = evaluate()
    md = render(r)
    (ROOT / "benchmark" / "RESULTS.md").write_text(md, encoding="utf-8")
    if "--quiet" not in argv:
        print(md)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
